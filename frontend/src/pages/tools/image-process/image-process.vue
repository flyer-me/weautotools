  { name: '朋友圈封面', width: 1080, height: 608 },
  { name: '720P', width: 1280, height: 720 },
  { name: '1080P', width: 1920, height: 1080 },
  { name: '淘宝主图', width: 800, height: 800 },
  { name: '公众号封面', width: 900, height: 500 },
  { name: '无限制', width: null, height: null }
